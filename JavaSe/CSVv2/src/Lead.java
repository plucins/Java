
public class Lead {

    private String calculationNumber;
    private String ug;
    private String telNumber;
    private String calculationLink;
    private String clausules;

    public Lead() {
    }

    public String getCalculationNumber() {
        return calculationNumber;
    }

    public void setCalculationNumber(String calculationNumber) {
        this.calculationNumber = calculationNumber;
    }

    public String getUg() {
        return ug;
    }

    public void setUg(String ug) {
        this.ug = ug;
    }

    public String getTelNumber() {
        return telNumber;
    }

    public void setTelNumber(String telNumber) {
        this.telNumber = telNumber;
    }

    public String getCalculationLink() {
        return calculationLink;
    }

    public void setCalculationLink(String calculationLink) {
        this.calculationLink = calculationLink;
    }

    public String getClausules() {
        return clausules;
    }

    public void setClausules(String clausules) {
        this.clausules = clausules;
    }
}
